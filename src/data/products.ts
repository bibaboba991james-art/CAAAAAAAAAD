// Функция валидации
export function validateProducts() {
  const productIds = products.map(p => p.id);
  const duplicateIds = productIds.filter((id, index) => productIds.indexOf(id) !== index);
  
  const categoryIds = categories.map(c => c.id);
  const invalidCategories = products.filter(p => !categoryIds.includes(p.category));
  
  return {
    totalProducts: products.length,
    totalCategories: categories.length,
    duplicateIds,
    invalidCategories: invalidCategories.map(p => ({ id: p.id, category: p.category }))
  };
}
